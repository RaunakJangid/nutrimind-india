def validate_food_map_targets(ifct_path: Path | str | None = None) -> list[str]:
    """Return FOOD_MAP target keys missing from processed IFCT data."""
    root = Path(__file__).resolve().parents[1]
    path = Path(ifct_path) if ifct_path else root / "data" / "processed" / "ifct2017.csv"
    keys = set(pd.read_csv(path)["food_key"].astype(str))
    return sorted({target for target in FOOD_MAP.values()} - keys)
